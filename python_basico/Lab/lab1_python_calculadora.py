# Python como uma calculadora

print(2 + 2)
print(2 / 2)
print(2 - 2)
print(2 / 3)
print(2 // 3)
print(7 % 2)
print(5 ** 2)

# Exponenciação
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# Multiplicação
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

# Divisão
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

# Divisão de número inteiro (divisão arredondada)
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print(6 // 4)
print(6. // 4)

# Teste
print((2 ** 4), (2 * 4.), (2 * 4))
print((-2 / 4), (2 / 4), (2 // 4), (-2 // 4))
print((2 % -4), (2 % 4), (2 ** 3 ** 2))
