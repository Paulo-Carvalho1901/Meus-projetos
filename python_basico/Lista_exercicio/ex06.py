"""
Escreva um programa que leia um
valor em metros e o exiba convertido em centimentros
e milimetros.
"""

valor = int(input('Digite o valor em metros: '))

print(f'O valor de metros foi {valor}m e em cintimentro é {valor * 100}cm')
print(f'O valor em metros foi {valor}m e em milimetro é {valor * 1000}mm')
