# Variáveis são espaços na memória que guardam dados.


# Criando variáveis

nome = "Gabriel"
idade = 38
cidade = "Brasília"


# Podemos mostrar o valor de uma variável usando print()

print(nome)
print(idade)
print(cidade)


# Também podemos juntar texto com variáveis

print("Meu nome é", nome)
print("Tenho", idade, "anos")
print("Moro em", cidade)


